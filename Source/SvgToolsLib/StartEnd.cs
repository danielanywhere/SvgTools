namespace SvgToolsLibrary
{
	//*-------------------------------------------------------------------------*
	//*	StartEndCollection																											*
	//*-------------------------------------------------------------------------*
	/// <summary>
	/// Collection of StartEndItem Items.
	/// </summary>
	public class StartEndCollection : List<StartEndItem>
	{
		//*************************************************************************
		//*	Private																																*
		//*************************************************************************
		//*************************************************************************
		//*	Protected																															*
		//*************************************************************************
		//*************************************************************************
